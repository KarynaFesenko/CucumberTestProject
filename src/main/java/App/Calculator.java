package App;

public class Calculator {

    public int addTwoNumbers(int firstNum, int secNum) {
        return firstNum + secNum;
    }

    public int multiplication(int firstNum, int secNum) {
        return firstNum * secNum;
    }

    public int division(int firstNum, int secNum) {
        return firstNum / secNum;
    }

    public int subtraction(int firstNum, int secNum) {
        return firstNum - secNum;
    }
}


